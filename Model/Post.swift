«Горизонтальная плоскость проходит горизонтально, под прямым углом как к фронтальной, так и сагиттальной плоскости. Она делит тело на верхний и нижний отделы». (М.Г. Ткачук, И.А. Степаник, 2010).

Движение в горизонтальной плоскости проходит вокруг вертикальной оси вращения.
Вертикальная(продольная) ось — образуется при пересечении сагиттальной и фронтальной плоскостей. При вращении вокруг вертикальной оси движения происходят строго в горизонтальной плоскости.
""")
            
        ]
                  
    }
}
