struct Post {
    
    let title: String
    let subTitle: String
    let image: UIImage?
    let image_move: UIImage?
    let description: String
    
}

extension Post {
    static func make() -> [Post] {
        
        [
            Post(title: "Сагиттальная плоскость",
                 subTitle: "Фронтальная ось",
                 image: UIImage(named: "sagittal"),
                 image_move: UIImage(named: "movement_sagittal"),
                  description: """
«Сагиттальная плоскость проходит через тело в передне-заднем направлении, её название произошло от слова sagitta – стрела. Сагиттальная плоскость, проходящая строго посередине тела и делящая его на правую и левую половины, называется срединной, или медианной» (М.Ф. Иваницкий, 1965).

«Вертикальная плоскость, проходящая через переднюю срединную и позвоночную линии, а также всякая плоскость параллельная ей называются сагиттальными. Они разделяют тело на правую и левую части» (В.М. Зациорский, А.С. Аруин, В.Н. Селуянов, 1981).

«Под сагиттальной плоскостью (от лат. sagita – «стрела») понимается вертикальная плоскость, ориентированная спереди назад, которой мы мысленно рассекаем тело в направлении пронзающей его стрелы. Сагиттальная плоскость проходит посередине тела и делит его на две симметричные половины – правую и левую» (М.Г. Ткачук, И.А. Степаник, 2010)

Движения в сагиттальной плоскости проходят вокруг фронтальной оси вращения.
Фронтальная(поперечная) ось — образуется при пересечении фронтальной и горизонтальной плоскостей. Вращение вокруг фронтальной оси осуществляется в сагиттальной плоскости.
"""),
            
