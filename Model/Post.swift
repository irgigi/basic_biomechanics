                 image_move: UIImage(named: "movement_frontal"),
                 description: """
«…фронтальная плоскость (делит тело – А.В. Самсонова) в поперечном направлении, соответствуя плоскости лба, откуда произошло и её название (fronts – лоб, frontalis – лобный» (М.Ф. Иваницкий, 1965)

«Вертикальная плоскость, проходящая перпендикулярно к сагиттальной, а также всякая плоскость параллельная ей, называются фронтальными. Они разделяют тело на переднюю и заднюю части» (В.М. Зациорский, А.С. Аруин, В.Н. Селуянов, 1981).

«Фронтальная плоскость (от лат. fronts – «лоб») тоже идет вертикально, перпендикулярно сагиттальной плоскости, ориентирована параллельно лбу (М.Г. Ткачук, И.А. Степаник, 2010).

Движение во фронтальной плоскости проходит вокруг сагиттальной оси вращения.
Сагиттальная ось — образуется при пересечении горизонтальной и сагиттальной плоскостей. При вращении части тела вокруг этой оси движение происходит строго во фронтальной плоскости
"""),
            
            Post(title: "Горизонтальная плоскость",
